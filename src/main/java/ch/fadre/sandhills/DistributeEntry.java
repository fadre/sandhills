package ch.fadre.sandhills;

public class DistributeEntry {

    private final int x;
    private final int y;

    public DistributeEntry(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
